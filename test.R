# Cumulative Distribution
pnorm(0)
pbinom(5, 10, 0.5)
pgeom(5, 0.3)
ppois(2, 3)
punif(4, 2, 8)
pexp(0.3)
pgamma(2, 7)

# Quantile (inverse c.d.f.)
qnorm()
qbinom()
qgeom()
qpois()
qunif()
qexp()
qgamma()

# Density Function
dnorm()
dbinom()
dgeom()
dpois()
dunif()
dexp()
dgamma()

# Random Variable from distribution (n=num samples)
rnorm(n=1100)
rbinom()
rgeom()
rpois()
runif()
rexp()
rgamma()
